import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { query } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get('LOVABLE_API_KEY');
    
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    console.log('Searching for:', query);

    const systemPrompt = `You are an expert assistant specializing in Italian immigration and bureaucracy. 
You have extensive knowledge of:
- Official Italian government procedures (Questura, Prefettura, Comune)
- Permesso di soggiorno (residence permit) processes
- Visa applications and renewals
- Codice fiscale and tessera sanitaria
- Housing contracts and residency registration
- Work permits and employment regulations
- Student visas and university enrollment
- Family reunification procedures
- Citizenship applications (naturalizzazione)
- Common issues and practical solutions from immigrant communities (Reddit, Facebook groups, Expat forums)

When answering:
1. Provide official procedures first
2. Include practical "street knowledge" tips from immigrant communities
3. Mention common pitfalls and how to avoid them
4. List relevant official websites and resources
5. Give time estimates based on real experiences
6. Format with clear sections and bullet points`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { role: "system", content: systemPrompt },
          { role: "user", content: `Search query: "${query}"\n\nProvide comprehensive information about this Italian immigration/bureaucracy topic. Include official procedures, community tips, and practical advice.` }
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please try again later." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "Service temporarily unavailable." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      
      throw new Error(`AI gateway error: ${response.status}`);
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (error: unknown) {
    console.error("Search error:", error);
    const errorMessage = error instanceof Error ? error.message : "Unknown error";
    return new Response(JSON.stringify({ error: errorMessage }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
